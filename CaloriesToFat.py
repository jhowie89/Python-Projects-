#Calories from fat and carbs

fat = float(input("Enter amount of fat grams for the day "))
carb = float (input("Enter amount of carbohydrate grams for the day "))

fat_calories = fat * 9
carb_calories = carb * 4

print("The amount of calories from the fat is " , fat_calories)
print("The amount of carbs from the fat is " , carb_calories )
